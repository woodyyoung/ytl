public class StretchPlatResultVo {

	private String stretchId;

	private String platFormId;

	private String numbered;
	
	private String name;

	private String described;
	
	private String direct;
	
	private String remark;

	public String getStretchId() {
		return stretchId;
	}

	public void setStretchId(String stretchId) {
		this.stretchId = stretchId;
	}

	public String getPlatFormId() {
		return platFormId;
	}

	public void setPlatFormId(String platFormId) {
		this.platFormId = platFormId;
	}

	public String getNumbered() {
		return numbered;
	}

	public void setNumbered(String numbered) {
		this.numbered = numbered;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescribed() {
		return described;
	}

	public void setDescribed(String described) {
		this.described = described;
	}

	public String getDirect() {
		return direct;
	}

	public void setDirect(String direct) {
		this.direct = direct;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
	
}
